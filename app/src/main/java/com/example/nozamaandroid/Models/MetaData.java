package com.example.nozamaandroid.Models;

public class MetaData {
    String metaName, metaUplTime, metaSize, metaType;

    public String getMetaName() {
        return metaName;
    }

    public void setMetaName(String metaName) {
        this.metaName = metaName;
    }

    public String getMetaUplTime() {
        return metaUplTime;
    }

    public void setMetaUplTime(String metaUplTime) {
        this.metaUplTime = metaUplTime;
    }

    public String getMetaSize() {
        return metaSize;
    }

    public void setMetaSize(String metaSize) {
        this.metaSize = metaSize;
    }

    public String getMetaType() {
        return metaType;
    }

    public void setMetaType(String metaType) {
        this.metaType = metaType;
    }
}
